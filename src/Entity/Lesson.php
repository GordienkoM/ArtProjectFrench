<?php

namespace App\Entity;

use App\Repository\LessonRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=LessonRepository::class)
 */
class Lesson
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="text")
     */
    private $title;

    /**
     * @ORM\Column(type="integer")
     */
    private $price;

    /**
     * @ORM\Column(type="text")
     */
    private $description;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     */
    private $image;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     */
    private $video;

    /**
     * @ORM\ManyToMany(targetEntity=Course::class, mappedBy="lessons")
     */
    private $courses;

    /**
     * @ORM\ManyToMany(targetEntity=OrderRequest::class, mappedBy="lessons")
     */
    private $orderRequests;

    public function __construct()
    {
        $this->courses = new ArrayCollection();
        $this->orderRequests = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getPrice(): ?int
    {
        return $this->price;
    }

    public function setPrice(int $price): self
    {
        $this->price = $price;

        return $this;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

    public function setDescription(string $description): self
    {
        $this->description = $description;

        return $this;
    }

    public function getImage(): ?string
    {
        return $this->image;
    }

    public function setImage(?string $image): self
    {
        $this->image = $image;

        return $this;
    }

    public function getVideo(): ?string
    {
        return $this->video;
    }

    public function setVideo(?string $video): self
    {
        $this->video = $video;

        return $this;
    }

    /**
     * @return Collection|Course[]
     */
    public function getCourses(): Collection
    {
        return $this->courses;
    }

    public function addCourse(Course $course): self
    {
        if (!$this->courses->contains($course)) {
            $this->courses[] = $course;
            $course->addLesson($this);
        }

        return $this;
    }

    public function removeCourse(Course $course): self
    {
        if ($this->courses->removeElement($course)) {
            $course->removeLesson($this);
        }

        return $this;
    }

    /**
     * @return Collection|OrderRequest[]
     */
    public function getOrderRequests(): Collection
    {
        return $this->orderRequests;
    }

    public function addOrderRequest(OrderRequest $orderRequest): self
    {
        if (!$this->orderRequests->contains($orderRequest)) {
            $this->orderRequests[] = $orderRequest;
            $orderRequest->addLesson($this);
        }

        return $this;
    }

    public function removeOrderRequest(OrderRequest $orderRequest): self
    {
        if ($this->orderRequests->removeElement($orderRequest)) {
            $orderRequest->removeLesson($this);
        }

        return $this;
    }
}
